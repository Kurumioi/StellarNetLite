        #region ModuleBinder 分片

        private static List<GeneratedFileRecord> BuildModuleBinderShardFiles(List<ClassMeta> serverMods, List<ClassMeta> clientMods)
        {
            var result = new List<GeneratedFileRecord>();
            var featureKeys = serverMods.Concat(clientMods)
                .Select(m => string.IsNullOrEmpty(m.FeatureKey) ? "Unknown" : SanitizeIdentifier(m.FeatureKey)).Distinct().OrderBy(k => k).ToList();

            foreach (string featureKey in featureKeys)
            {
                var featureServerMods = serverMods.Where(m => SanitizeIdentifier(m.FeatureKey) == featureKey).OrderBy(m => m.FullName).ToList();
                var featureClientMods = clientMods.Where(m => SanitizeIdentifier(m.FeatureKey) == featureKey).OrderBy(m => m.FullName).ToList();

                var sRegSb = new StringBuilder();
                foreach (var mod in featureServerMods)
