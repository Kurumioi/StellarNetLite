        private static ScanResultState ScanAndGenerateComponentsAndRegistry(bool forceWrite)
        {
            var serverComps = new List<ClassMeta>();
            var clientComps = new List<ClassMeta>();
            var serverMods = new List<ClassMeta>();
            var clientMods = new List<ClassMeta>();

            var serverGlobalMsgIds = new Dictionary<int, string>();
            var serverRoomMsgIds = new Dictionary<int, string>();
            var clientGlobalMsgIds = new Dictionary<int, string>();
            var clientRoomMsgIds = new Dictionary<int, string>();

            bool hasFatalError = false;

            foreach (Type type in TypeCache.GetTypesWithAttribute<RoomComponentAttribute>())
            {
                var attr = type.GetCustomAttribute<RoomComponentAttribute>();
                if (attr == null) continue;

                var meta = new ClassMeta
                {
                    Id = attr.Id, Name = attr.Name, SafeVarName = SanitizeIdentifier(GetCSharpTypeName(type)), FullName = GetCSharpTypeName(type),
                    DisplayName = attr.DisplayName, FeatureKey = GetFeatureKeyByType(type)
                };

                bool isServer = type.IsSubclassOf(typeof(StellarNet.Lite.Server.Core.ServerRoomComponent));
                bool isClient = type.IsSubclassOf(typeof(StellarNet.Lite.Client.Core.ClientRoomComponent));

                if (!isServer && !isClient)
                {
                    hasFatalError = true;
                    continue;
                }

                if (!ScanMethods(type, isServer, meta, isServer ? serverRoomMsgIds : clientRoomMsgIds)) hasFatalError = true;

                if (isServer) serverComps.Add(meta);
                else clientComps.Add(meta);
            }

            foreach (Type type in TypeCache.GetTypesWithAttribute<ServerModuleAttribute>())
            {
                var attr = type.GetCustomAttribute<ServerModuleAttribute>();
                if (attr == null) continue;

                var meta = new ClassMeta
                {
                    Name = attr.Name, SafeVarName = SanitizeIdentifier(GetCSharpTypeName(type)), FullName = GetCSharpTypeName(type),
                    DisplayName = attr.DisplayName, FeatureKey = GetFeatureKeyByType(type)
                };

                if (!ScanMethods(type, true, meta, serverGlobalMsgIds)) hasFatalError = true;
                serverMods.Add(meta);
            }

            foreach (Type type in TypeCache.GetTypesWithAttribute<ClientModuleAttribute>())
            {
                var attr = type.GetCustomAttribute<ClientModuleAttribute>();
                if (attr == null) continue;

                var meta = new ClassMeta
                {
                    Name = attr.Name, SafeVarName = SanitizeIdentifier(GetCSharpTypeName(type)), FullName = GetCSharpTypeName(type),
                    DisplayName = attr.DisplayName, FeatureKey = GetFeatureKeyByType(type)
                };

                if (!ScanMethods(type, false, meta, clientGlobalMsgIds)) hasFatalError = true;
                clientMods.Add(meta);
            }

            if (hasFatalError) return ScanResultState.Failed;

            var mergedComps = MergeUniqueComponentsById(serverComps, clientComps);

            bool c1 = WriteGeneratedShardSet(ComponentConstFolderPath, BuildComponentConstShardFiles(mergedComps), forceWrite);
            bool c2 = WriteGeneratedShardSet(BinderRoomFolderPath, BuildRoomBinderShardFiles(serverComps, clientComps, mergedComps), forceWrite);
            bool c3 = WriteGeneratedShardSet(BinderModuleFolderPath, BuildModuleBinderShardFiles(serverMods, clientMods), forceWrite);
            bool c4 = GenerateAggregateComponentConstFile(mergedComps, forceWrite);
            bool c5 = GenerateAggregateAutoRegistryFile(serverComps, clientComps, serverMods, clientMods, mergedComps, forceWrite);

            return (c1 || c2 || c3 || c4 || c5) ? ScanResultState.Changed : ScanResultState.NoChange;
        }

        private static bool ScanMethods(Type type, bool isServer, ClassMeta meta, Dictionary<int, string> msgIdTracker)
        {
            bool isSafe = true;
            foreach (MethodInfo method in type.GetMethods(BindingFlags.Instance | BindingFlags.Public | BindingFlags.NonPublic))
            {
                if (method.GetCustomAttribute<NetHandlerAttribute>() == null) continue;

                var parameters = method.GetParameters();
                Type msgType = isServer
                    ? (parameters.Length == 2 ? parameters[1].ParameterType : null)
                    : (parameters.Length == 1 ? parameters[0].ParameterType : null);

                if (msgType == null)
                {
                    isSafe = false;
                    continue;
                }

                var netMsgAttr = msgType.GetCustomAttribute<NetMsgAttribute>();
                if (netMsgAttr == null)
                {
                    isSafe = false;
                    continue;
                }

                if (msgIdTracker.ContainsKey(netMsgAttr.Id))
                {
                    isSafe = false;
                    continue;
                }

                msgIdTracker.Add(netMsgAttr.Id, $"{type.FullName}.{method.Name}");

                meta.Methods.Add(new MethodMeta
                {
                    MethodName = method.Name, MsgFullName = GetCSharpTypeName(msgType), MsgId = netMsgAttr.Id,
                    FeatureKey = GetFeatureKeyByType(msgType)
                });
            }

            return isSafe;
        }

        private static List<ClassMeta> MergeUniqueComponentsById(List<ClassMeta> serverComps, List<ClassMeta> clientComps)
        {
            var merged = new List<ClassMeta>();
            var idMap = new HashSet<int>();
            foreach (var meta in serverComps.Concat(clientComps).OrderBy(c => c.Id))
            {
                if (idMap.Add(meta.Id)) merged.Add(meta);
            }

            return merged;
        }

