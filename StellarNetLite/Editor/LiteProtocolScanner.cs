        private static string GetCSharpTypeName(Type type)
        {
            if (type == null) return "void";
            if (type.IsByRef) return "ref " + GetCSharpTypeName(type.GetElementType());
            if (type.IsPointer) return GetCSharpTypeName(type.GetElementType()) + "*";
            if (type.IsGenericParameter) return type.Name;
            if (BuiltInTypeNames.TryGetValue(type, out string builtInName)) return builtInName;
            if (type.IsArray) return GetCSharpTypeName(type.GetElementType()) + $"[{new string(',', type.GetArrayRank() - 1)}]";

            if (type.IsGenericType)
            {
                string genericName = type.GetGenericTypeDefinition().FullName ?? type.Name;
                int tickIndex = genericName.IndexOf('`');
                if (tickIndex >= 0) genericName = genericName.Substring(0, tickIndex);
                genericName = genericName.Replace('+', '.');
                string[] argNames = type.GetGenericArguments().Select(GetCSharpTypeName).ToArray();
                return $"{genericName}<{string.Join(", ", argNames)}>";
            }

            return !string.IsNullOrEmpty(type.FullName) ? type.FullName.Replace('+', '.') : type.Name;
        }

        private static string SanitizeIdentifier(string name)
        {
            if (string.IsNullOrEmpty(name)) return "Unknown";
            string safe = Regex.Replace(name, @"[^a-zA-Z0-9_]", "_");
            if (string.IsNullOrEmpty(safe)) return "Unknown";
            return char.IsDigit(safe[0]) ? "_" + safe : safe;
        }

        private static string EscapeStringLiteral(string value) =>
            string.IsNullOrEmpty(value) ? string.Empty : value.Replace("\\", "\\\\").Replace("\"", "\\\"");

        private static string GetFeatureKeyByType(Type type)
        {
            if (type == null) return "Unknown";
            string featureKey = GetFeatureKeyFromSourceHeader(type);
            return !string.IsNullOrEmpty(featureKey) ? SanitizeIdentifier(featureKey) : GetFeatureKeyByNamespaceFallback(type);
        }

        private static string GetFeatureKeyFromSourceHeader(Type type)
        {
            MonoScript monoScript = FindMonoScriptByType(type);
            if (monoScript == null) return string.Empty;

            string fullPath = AssetPathToFullPath(AssetDatabase.GetAssetPath(monoScript));
            if (string.IsNullOrEmpty(fullPath) || !File.Exists(fullPath)) return string.Empty;

            string[] lines = File.ReadAllLines(fullPath);
            for (int i = 0; i < Math.Min(12, lines.Length); i++)
            {
                string trimmed = lines[i]?.Trim();
                if (trimmed != null && trimmed.StartsWith(AutoGeneratedFeatureMarkerPrefix, StringComparison.Ordinal))
                {
                    return trimmed.Substring(AutoGeneratedFeatureMarkerPrefix.Length).Trim();
                }
            }

            return string.Empty;
        }

        private static MonoScript FindMonoScriptByType(Type type)
        {
            string[] guids = AssetDatabase.FindAssets($"{type.Name} t:MonoScript");
            foreach (string guid in guids)
            {
                MonoScript script = AssetDatabase.LoadAssetAtPath<MonoScript>(AssetDatabase.GUIDToAssetPath(guid));
                if (script != null && script.GetClass() == type) return script;
            }

            return null;
        }

        private static string GetFeatureKeyByNamespaceFallback(Type type)
        {
            string ns = type.Namespace ?? string.Empty;
            if (string.IsNullOrEmpty(ns)) return SanitizeIdentifier(type.Name);

            string[] segments = ns.Split('.');
            for (int i = 0; i < segments.Length - 1; i++)
            {
                if ((segments[i] == "Shared" && segments[i + 1] == "Protocol") ||
                    (segments[i] == "Server" && (segments[i + 1] == "Components" || segments[i + 1] == "Modules")) ||
                    (segments[i] == "Client" && (segments[i + 1] == "Components" || segments[i + 1] == "Modules")))
                {
                    return i - 1 >= 0 ? SanitizeIdentifier(segments[i - 1]) : SanitizeIdentifier(type.Name);
                }
            }

            return segments.Length >= 2 ? SanitizeIdentifier(segments[segments.Length - 1]) : SanitizeIdentifier(type.Name);
        }

        private static string AssetPathToFullPath(string assetPath)
        {
            if (string.IsNullOrEmpty(assetPath)) return string.Empty;
            string normalized = NormalizePath(assetPath);
            if (!normalized.StartsWith("Assets", StringComparison.Ordinal)) return string.Empty;

            DirectoryInfo root = Directory.GetParent(Application.dataPath);
            return root != null ? NormalizePath($"{root.FullName.Replace("\\", "/")}/{normalized}") : string.Empty;
        }

