        #region 分片写入与清理

        private static bool WriteGeneratedShardSet(string folderPath, List<GeneratedFileRecord> records,
            bool forceWrite)
        {
            if (string.IsNullOrEmpty(folderPath) || records == null)
            {
                NetLogger.LogError("LiteProtocolScanner", "WriteGeneratedShardSet failed");
                return false;
            }

            EnsureDirectoryExists(folderPath);

            bool changed = false;
            var expectedPaths = new HashSet<string>(records.Select(r => NormalizePath(r.Path)));
            string[] oldFiles = Directory.Exists(folderPath)
                ? Directory.GetFiles(folderPath, "*.cs", SearchOption.TopDirectoryOnly)
                : Array.Empty<string>();

            for (int i = 0; i < oldFiles.Length; i++)
            {
                string oldFile = NormalizePath(oldFiles[i]);
                if (expectedPaths.Contains(oldFile))
                {
                    continue;
                }

                File.Delete(oldFile);
                string metaPath = oldFile + ".meta";
                if (File.Exists(metaPath))
                {
                    File.Delete(metaPath);
                }

                changed = true;
            }

            for (int i = 0; i < records.Count; i++)
            {
                GeneratedFileRecord record = records[i];
                if (record == null)
                {
                    NetLogger.LogError("LiteProtocolScanner", "WriteGeneratedShardSet failed");
                    continue;
                }

                bool fileChanged = WriteToFile(record.Path, record.Content, forceWrite);
                if (fileChanged)
                {
                    changed = true;
                }
            }

            return changed;
        }

        private static void EnsureDirectoryExists(string folderPath)
        {
            if (string.IsNullOrEmpty(folderPath))
            {
                NetLogger.LogError("LiteProtocolScanner", "EnsureDirectoryExists failed");
                return;
            }

            if (!Directory.Exists(folderPath))
            {
                Directory.CreateDirectory(folderPath);
            }
        }

        private static string NormalizePath(string path)
        {
            if (string.IsNullOrEmpty(path))
            {
                return string.Empty;
            }

            return path.Replace("\\", "/");
        }

        #endregion

        #region 文件写入

        private static bool WriteToFile(string path, string newContent, bool forceWrite)
        {
            if (string.IsNullOrEmpty(path) || newContent == null)
            {
                NetLogger.LogError("LiteProtocolScanner", "WriteToFile failed");
                return false;
            }

            string normalizedPath = NormalizePath(path);
            string directory = Path.GetDirectoryName(normalizedPath);
            if (string.IsNullOrEmpty(directory))
            {
                NetLogger.LogError("LiteProtocolScanner", "WriteToFile failed");
                return false;
            }

            if (!Directory.Exists(directory))
            {
                Directory.CreateDirectory(directory);
            }

            if (!forceWrite)
            {
                string oldContent = File.Exists(normalizedPath) ? File.ReadAllText(normalizedPath) : string.Empty;
                if (newContent == oldContent)
                {
                    return false;
                }
            }

            File.WriteAllText(normalizedPath, newContent, Encoding.UTF8);
            return true;
        }

