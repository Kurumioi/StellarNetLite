        private static List<GeneratedFileRecord> BuildRoomBinderShardFiles(List<ClassMeta> serverComps, List<ClassMeta> clientComps,
            List<ClassMeta> mergedComps)
        {
            var result = new List<GeneratedFileRecord>();
            var featureKeys = mergedComps.Select(c => string.IsNullOrEmpty(c.FeatureKey) ? "Unknown" : SanitizeIdentifier(c.FeatureKey)).Distinct()
                .OrderBy(k => k).ToList();

            foreach (string featureKey in featureKeys)
            {
                var featureMergedComps = mergedComps.Where(c => SanitizeIdentifier(c.FeatureKey) == featureKey).OrderBy(c => c.Id).ToList();
                var featureServerComps = serverComps.Where(c => SanitizeIdentifier(c.FeatureKey) == featureKey).OrderBy(c => c.Id).ToList();
                var featureClientComps = clientComps.Where(c => SanitizeIdentifier(c.FeatureKey) == featureKey).OrderBy(c => c.Id).ToList();

                var metaSb = new StringBuilder();
                foreach (var comp in featureMergedComps)
                    metaSb.AppendLine(
                        $"            target.Add(new RoomComponentMeta {{ Id = {comp.Id}, Name = \"{EscapeStringLiteral(comp.Name)}\", DisplayName = \"{EscapeStringLiteral(comp.DisplayName)}\" }});");

                var sRegSb = new StringBuilder();
                var sBindSb = new StringBuilder();
                foreach (var comp in featureServerComps)
                {
                    sRegSb.AppendLine($"            ServerRoomFactory.Register({comp.Id}, () => new {comp.FullName}(serverApp));");

                    sBindSb.AppendLine($"                case {comp.FullName} c_{comp.Id}:");
                    foreach (var method in comp.Methods)
                    {
                        sBindSb.AppendLine($@"                    dispatcher.Register({method.MsgId}, (session, packet) => {{
                        if (deserializeFunc(packet.Payload, packet.PayloadOffset, packet.PayloadLength, typeof({method.MsgFullName})) is {method.MsgFullName} msg) {{
                            c_{comp.Id}.{method.MethodName}(session, msg);
                        }}
                    }});");
                    }

                    sBindSb.AppendLine("                    return true;");
                }

                var cRegSb = new StringBuilder();
                var cBindSb = new StringBuilder();
                foreach (var comp in featureClientComps)
                {
                    cRegSb.AppendLine($"            ClientRoomFactory.Register({comp.Id}, () => new {comp.FullName}(clientApp));");

                    cBindSb.AppendLine($"                case {comp.FullName} c_{comp.Id}:");
                    foreach (var method in comp.Methods)
                    {
                        cBindSb.AppendLine($@"                    dispatcher.Register({method.MsgId}, (packet) => {{
                        if (deserializeFunc(packet.Payload, packet.PayloadOffset, packet.PayloadLength, typeof({method.MsgFullName})) is {method.MsgFullName} msg) {{
                            c_{comp.Id}.{method.MethodName}(msg);
                        }}
                    }});");
                    }

                    cBindSb.AppendLine("                    return true;");
                }

                string content = $@"using System;
using System.Collections.Generic;
using StellarNet.Lite.Shared.Core;
using StellarNet.Lite.Server.Core;
using StellarNet.Lite.Client.Core;

namespace StellarNet.Lite.Shared.Generated.Binders.RoomComponents
{{
    public static class Generated_{featureKey}_RoomBinder
    {{
        public static void AppendRoomComponentMeta(List<RoomComponentMeta> target)
        {{
            if (target == null) return;
{metaSb}        }}

        public static void RegisterServerFactory(ServerApp serverApp)
        {{
            if (serverApp == null) return;
{sRegSb}        }}

        public static void RegisterClientFactory(ClientApp clientApp)
        {{
            if (clientApp == null) return;
{cRegSb}        }}

        public static bool TryBindServer(StellarNet.Lite.Server.Core.ServerRoomComponent comp, StellarNet.Lite.Server.Core.RoomDispatcher dispatcher, Func<byte[], int, int, Type, object> deserializeFunc)
        {{
            if (comp == null || dispatcher == null || deserializeFunc == null) return false;

            switch (comp)
            {{
{sBindSb}            }}
            return false;
        }}

        public static bool TryBindClient(StellarNet.Lite.Client.Core.ClientRoomComponent comp, StellarNet.Lite.Client.Core.ClientRoomDispatcher dispatcher, Func<byte[], int, int, Type, object> deserializeFunc)
        {{
            if (comp == null || dispatcher == null || deserializeFunc == null) return false;

            switch (comp)
            {{
{cBindSb}            }}
            return false;
        }}
    }}
}}";
                result.Add(new GeneratedFileRecord { Path = $"{BinderRoomFolderPath}/Generated_{featureKey}_RoomBinder.cs", Content = content });
            }

            return result;
        }

