        private static ScanResultState ScanAndGenerateComponentsAndRegistry(bool forceWrite)
        {
            var serverComps = new List<ClassMeta>();
            var clientComps = new List<ClassMeta>();
            var serverMods = new List<ClassMeta>();
            var clientMods = new List<ClassMeta>();

            var serverGlobalMsgIds = new Dictionary<int, string>();
            var serverRoomMsgIds = new Dictionary<int, string>();
            var clientGlobalMsgIds = new Dictionary<int, string>();
            var clientRoomMsgIds = new Dictionary<int, string>();

            bool hasFatalError = false;

            var roomCompTypes = TypeCache.GetTypesWithAttribute<RoomComponentAttribute>();
            foreach (Type type in roomCompTypes)
            {
                if (type == null)
                {
                    continue;
                }

                RoomComponentAttribute attr = type.GetCustomAttribute<RoomComponentAttribute>();
                if (attr == null)
                {
                    continue;
                }

                var meta = new ClassMeta
                {
                    Id = attr.Id,
                    Name = attr.Name,
                    SafeVarName = SanitizeIdentifier(GetCSharpTypeName(type)),
                    FullName = GetCSharpTypeName(type),
                    DisplayName = attr.DisplayName,
                    FeatureKey = GetFeatureKeyByType(type)
                };

                bool isServer = type.IsSubclassOf(typeof(StellarNet.Lite.Server.Core.RoomComponent));
                bool isClient = type.IsSubclassOf(typeof(StellarNet.Lite.Client.Core.ClientRoomComponent));

                if (!isServer && !isClient)
                {
                    NetLogger.LogError("LiteProtocolScanner", "RoomComponent base type invalid");
                    hasFatalError = true;
                    continue;
                }

                if (isServer && isClient)
                {
                    NetLogger.LogError("LiteProtocolScanner", "RoomComponent base type conflict");
                    hasFatalError = true;
                    continue;
                }

                bool methodsSafe = ScanMethods(type, isServer, meta, isServer ? serverRoomMsgIds : clientRoomMsgIds);
                if (!methodsSafe)
                {
                    hasFatalError = true;
                }

                if (isServer)
                {
                    serverComps.Add(meta);
                }
                else
                {
                    clientComps.Add(meta);
                }
            }

            var serverModTypes = TypeCache.GetTypesWithAttribute<ServerModuleAttribute>();
            foreach (Type type in serverModTypes)
            {
                if (type == null)
                {
                    continue;
                }

                ServerModuleAttribute attr = type.GetCustomAttribute<ServerModuleAttribute>();
                if (attr == null)
                {
                    continue;
                }

                var meta = new ClassMeta
                {
                    Name = attr.Name,
                    SafeVarName = SanitizeIdentifier(GetCSharpTypeName(type)),
                    FullName = GetCSharpTypeName(type),
                    DisplayName = attr.DisplayName,
                    FeatureKey = GetFeatureKeyByType(type)
                };

                if (!ScanMethods(type, true, meta, serverGlobalMsgIds))
                {
                    hasFatalError = true;
                }

                serverMods.Add(meta);
            }

            var clientModTypes = TypeCache.GetTypesWithAttribute<ClientModuleAttribute>();
            foreach (Type type in clientModTypes)
            {
                if (type == null)
                {
                    continue;
                }

                ClientModuleAttribute attr = type.GetCustomAttribute<ClientModuleAttribute>();
                if (attr == null)
                {
                    continue;
                }

                var meta = new ClassMeta
                {
                    Name = attr.Name,
                    SafeVarName = SanitizeIdentifier(GetCSharpTypeName(type)),
                    FullName = GetCSharpTypeName(type),
                    DisplayName = attr.DisplayName,
                    FeatureKey = GetFeatureKeyByType(type)
                };

                if (!ScanMethods(type, false, meta, clientGlobalMsgIds))
                {
                    hasFatalError = true;
                }

                clientMods.Add(meta);
            }

            if (!ValidateComponentMetaConsistency(serverComps, clientComps))
            {
                hasFatalError = true;
            }

            if (!ValidateComponentConstNameConflicts(serverComps, clientComps))
            {
                hasFatalError = true;
            }

            if (hasFatalError)
            {
                return ScanResultState.Failed;
            }

            List<ClassMeta> mergedComps = MergeUniqueComponentsById(serverComps, clientComps);

            List<GeneratedFileRecord> componentShardFiles = BuildComponentConstShardFiles(mergedComps);
            if (componentShardFiles == null)
            {
                return ScanResultState.Failed;
            }

            List<GeneratedFileRecord> roomBinderShardFiles =
                BuildRoomBinderShardFiles(serverComps, clientComps, mergedComps);
            if (roomBinderShardFiles == null)
            {
                return ScanResultState.Failed;
            }

            List<GeneratedFileRecord> moduleBinderShardFiles = BuildModuleBinderShardFiles(serverMods, clientMods);
            if (moduleBinderShardFiles == null)
            {
                return ScanResultState.Failed;
            }

            bool componentShardChanged =
                WriteGeneratedShardSet(ComponentConstFolderPath, componentShardFiles, forceWrite);
            bool roomBinderChanged = WriteGeneratedShardSet(BinderRoomFolderPath, roomBinderShardFiles, forceWrite);
            bool moduleBinderChanged =
                WriteGeneratedShardSet(BinderModuleFolderPath, moduleBinderShardFiles, forceWrite);
            bool componentAggregateChanged = GenerateAggregateComponentConstFile(mergedComps, forceWrite);
            bool registryAggregateChanged = GenerateAggregateAutoRegistryFile(serverComps, clientComps, serverMods,
                clientMods, mergedComps, forceWrite);

            bool anyChanged =
                componentShardChanged ||
                roomBinderChanged ||
                moduleBinderChanged ||
                componentAggregateChanged ||
                registryAggregateChanged;

            return anyChanged ? ScanResultState.Changed : ScanResultState.NoChange;
        }

        private static bool ScanMethods(Type type, bool isServer, ClassMeta meta, Dictionary<int, string> msgIdTracker)
        {
            if (type == null || meta == null || msgIdTracker == null)
            {
                NetLogger.LogError("LiteProtocolScanner", "ScanMethods failed");
                return false;
            }

            MethodInfo[] methods =
                type.GetMethods(BindingFlags.Instance | BindingFlags.Public | BindingFlags.NonPublic);
            bool isSafe = true;

            foreach (MethodInfo method in methods)
            {
                if (method.GetCustomAttribute<NetHandlerAttribute>() == null)
                {
                    continue;
                }

                ParameterInfo[] parameters = method.GetParameters();
                Type msgType = null;

                if (isServer)
                {
                    if (parameters.Length != 2 ||
                        parameters[0].ParameterType != typeof(StellarNet.Lite.Server.Core.Session))
                    {
                        NetLogger.LogError("LiteProtocolScanner", "Server NetHandler signature invalid");
                        isSafe = false;
                        continue;
                    }

                    msgType = parameters[1].ParameterType;
                }
                else
                {
                    if (parameters.Length != 1)
                    {
                        NetLogger.LogError("LiteProtocolScanner", "Client NetHandler signature invalid");
                        isSafe = false;
                        continue;
                    }

                    msgType = parameters[0].ParameterType;
                }

                if (msgType == null)
                {
                    NetLogger.LogError("LiteProtocolScanner", "Msg type invalid");
                    isSafe = false;
                    continue;
                }

                NetMsgAttribute netMsgAttr = msgType.GetCustomAttribute<NetMsgAttribute>();
                if (netMsgAttr == null)
                {
                    NetLogger.LogError("LiteProtocolScanner", "Msg type missing NetMsg");
                    isSafe = false;
                    continue;
                }

                string currentMethodName = $"{type.FullName}.{method.Name}";
                if (msgIdTracker.TryGetValue(netMsgAttr.Id, out _))
                {
                    NetLogger.LogError("LiteProtocolScanner", $"Handler MsgId conflict: {netMsgAttr.Id}");
                    isSafe = false;
                    continue;
                }

                msgIdTracker.Add(netMsgAttr.Id, currentMethodName);

                meta.Methods.Add(new MethodMeta
                {
                    MethodName = method.Name,
                    MsgFullName = GetCSharpTypeName(msgType),
                    MsgId = netMsgAttr.Id,
                    FeatureKey = GetFeatureKeyByType(msgType)
                });
            }

            return isSafe;
        }

        private static bool ValidateComponentMetaConsistency(List<ClassMeta> serverComps, List<ClassMeta> clientComps)
        {
            bool isSafe = true;

            var serverMap = new Dictionary<int, ClassMeta>();
            for (int i = 0; i < serverComps.Count; i++)
            {
                ClassMeta meta = serverComps[i];
                if (serverMap.ContainsKey(meta.Id))
                {
                    NetLogger.LogError("LiteProtocolScanner", $"Server component id conflict: {meta.Id}");
                    isSafe = false;
                    continue;
                }

                serverMap.Add(meta.Id, meta);
            }

            var clientMap = new Dictionary<int, ClassMeta>();
            for (int i = 0; i < clientComps.Count; i++)
            {
                ClassMeta meta = clientComps[i];
                if (clientMap.ContainsKey(meta.Id))
                {
                    NetLogger.LogError("LiteProtocolScanner", $"Client component id conflict: {meta.Id}");
                    isSafe = false;
                    continue;
                }

                clientMap.Add(meta.Id, meta);
            }

            foreach (KeyValuePair<int, ClassMeta> kv in serverMap)
            {
                if (!clientMap.TryGetValue(kv.Key, out ClassMeta clientMeta))
                {
                    continue;
                }

                ClassMeta serverMeta = kv.Value;
                if (!string.Equals(serverMeta.Name, clientMeta.Name, StringComparison.Ordinal) ||
                    !string.Equals(serverMeta.DisplayName, clientMeta.DisplayName, StringComparison.Ordinal))
                {
                    NetLogger.LogError("LiteProtocolScanner", $"Component meta mismatch: {kv.Key}");
                    isSafe = false;
                }
            }

            return isSafe;
        }

        private static bool ValidateComponentConstNameConflicts(List<ClassMeta> serverComps,
            List<ClassMeta> clientComps)
        {
            bool isSafe = true;
            List<ClassMeta> mergedComps = MergeUniqueComponentsById(serverComps, clientComps);
            var constNameMap = new Dictionary<string, ClassMeta>();

            for (int i = 0; i < mergedComps.Count; i++)
            {
                ClassMeta meta = mergedComps[i];
                string constName = SanitizeIdentifier(meta.Name);

                if (constNameMap.TryGetValue(constName, out ClassMeta existingMeta))
                {
                    bool isSameLogicalComponent =
                        existingMeta.Id == meta.Id &&
                        string.Equals(existingMeta.Name, meta.Name, StringComparison.Ordinal);

                    if (isSameLogicalComponent)
                    {
                        continue;
                    }

                    NetLogger.LogError("LiteProtocolScanner", $"Component const conflict: {constName}");
                    isSafe = false;
                    continue;
                }

                constNameMap.Add(constName, meta);
            }

            return isSafe;
        }

        private static List<ClassMeta> MergeUniqueComponentsById(List<ClassMeta> serverComps,
            List<ClassMeta> clientComps)
        {
            List<ClassMeta> allComps = serverComps.Concat(clientComps).OrderBy(c => c.Id).ToList();
            var merged = new List<ClassMeta>();
            var idMap = new Dictionary<int, ClassMeta>();

            for (int i = 0; i < allComps.Count; i++)
            {
                ClassMeta meta = allComps[i];
                if (meta == null)
                {
                    continue;
                }

                if (idMap.ContainsKey(meta.Id))
                {
                    continue;
                }

                idMap.Add(meta.Id, meta);
                merged.Add(meta);
            }

            return merged;
        }

