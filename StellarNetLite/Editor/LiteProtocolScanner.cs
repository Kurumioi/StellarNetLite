    public static class MsgIdConst
    {{
{sb}    }}
}}";
            return WriteToFile(MsgIdAggregateOutputPath, content, forceWrite);
        }

        private static bool GenerateAggregateMessageMetaRegistryFile(List<ProtocolMeta> protocolList, bool forceWrite)
        {
            var featureKeys = protocolList.Select(p => string.IsNullOrEmpty(p.FeatureKey) ? "Unknown" : SanitizeIdentifier(p.FeatureKey)).Distinct()
                .OrderBy(p => p).ToList();

            var sb1 = new StringBuilder();
            var sb2 = new StringBuilder();

            foreach (var key in featureKeys)
            {
                sb1.AppendLine($"            Generated_{key}_MessageMeta.AppendTypeToMeta(result);");
                sb2.AppendLine($"            Generated_{key}_MessageMeta.AppendMsgIdToType(result);");
            }

            string content = $@"using System;
