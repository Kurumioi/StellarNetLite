        private static string GetCSharpTypeName(Type type)
        {
            if (type == null)
            {
                return "void";
            }

            if (type.IsByRef)
            {
                Type elementType = type.GetElementType();
                return "ref " + GetCSharpTypeName(elementType);
            }

            if (type.IsPointer)
            {
                Type elementType = type.GetElementType();
                return GetCSharpTypeName(elementType) + "*";
            }

            if (type.IsGenericParameter)
            {
                return type.Name;
            }

            if (BuiltInTypeNames.TryGetValue(type, out string builtInName))
            {
                return builtInName;
            }

            if (type.IsArray)
            {
                int rank = type.GetArrayRank();
                string commas = new string(',', rank - 1);
                Type elementType = type.GetElementType();
                return GetCSharpTypeName(elementType) + $"[{commas}]";
            }

            if (type.IsGenericType)
            {
                string genericName = type.GetGenericTypeDefinition().FullName;
                if (!string.IsNullOrEmpty(genericName))
                {
                    int tickIndex = genericName.IndexOf('`');
                    if (tickIndex >= 0)
                    {
                        genericName = genericName.Substring(0, tickIndex);
                    }

                    genericName = genericName.Replace('+', '.');
                }
                else
                {
                    genericName = type.Name;
                    int tickIndex = genericName.IndexOf('`');
                    if (tickIndex >= 0)
                    {
                        genericName = genericName.Substring(0, tickIndex);
                    }
                }

                Type[] genericArgs = type.GetGenericArguments();
                string[] argNames = new string[genericArgs.Length];
                for (int i = 0; i < genericArgs.Length; i++)
                {
                    argNames[i] = GetCSharpTypeName(genericArgs[i]);
                }

                return $"{genericName}<{string.Join(", ", argNames)}>";
            }

            if (!string.IsNullOrEmpty(type.FullName))
            {
                return type.FullName.Replace('+', '.');
            }

            return type.Name;
        }

        private static string SanitizeIdentifier(string name)
        {
            if (string.IsNullOrEmpty(name))
            {
                return "Unknown";
            }

            string safe = Regex.Replace(name, @"[^a-zA-Z0-9_]", "_");
            if (string.IsNullOrEmpty(safe))
            {
                return "Unknown";
            }

            if (char.IsDigit(safe[0]))
            {
                safe = "_" + safe;
            }

            return safe;
        }

        private static string EscapeStringLiteral(string value)
        {
            if (string.IsNullOrEmpty(value))
            {
                return string.Empty;
            }

            return value.Replace("\\", "\\\\").Replace("\"", "\\\"");
        }

        private static string GetFeatureKeyByType(Type type)
        {
            if (type == null)
            {
                NetLogger.LogError("LiteProtocolScanner", "GetFeatureKey failed");
                return "Unknown";
            }

            string featureKey = GetFeatureKeyFromSourceHeader(type);
            if (!string.IsNullOrEmpty(featureKey))
            {
                return SanitizeIdentifier(featureKey);
            }

            return GetFeatureKeyByNamespaceFallback(type);
        }

        private static string GetFeatureKeyFromSourceHeader(Type type)
        {
            if (type == null)
            {
                NetLogger.LogError("LiteProtocolScanner", "GetFeatureKeyFromSourceHeader failed");
                return string.Empty;
            }

            MonoScript monoScript = FindMonoScriptByType(type);
            if (monoScript == null)
            {
                return string.Empty;
            }

            string assetPath = AssetDatabase.GetAssetPath(monoScript);
            if (string.IsNullOrEmpty(assetPath))
            {
                return string.Empty;
            }

            string fullPath = AssetPathToFullPath(assetPath);
            if (string.IsNullOrEmpty(fullPath))
            {
                return string.Empty;
            }

            if (!File.Exists(fullPath))
            {
                return string.Empty;
            }

            string[] lines = File.ReadAllLines(fullPath);
            if (lines == null || lines.Length == 0)
            {
                return string.Empty;
            }

            int maxCheckLineCount = Math.Min(12, lines.Length);
            for (int i = 0; i < maxCheckLineCount; i++)
            {
                string line = lines[i];
                if (string.IsNullOrWhiteSpace(line))
                {
                    continue;
                }

                string trimmed = line.Trim();
                if (!trimmed.StartsWith(AutoGeneratedFeatureMarkerPrefix, StringComparison.Ordinal))
                {
                    continue;
                }

                string rawFeature = trimmed.Substring(AutoGeneratedFeatureMarkerPrefix.Length).Trim();
                if (string.IsNullOrEmpty(rawFeature))
                {
                    continue;
                }

                return rawFeature;
            }

            return string.Empty;
        }

        private static MonoScript FindMonoScriptByType(Type type)
        {
            if (type == null)
            {
                NetLogger.LogError("LiteProtocolScanner", "FindMonoScriptByType failed");
                return null;
            }

            string[] guids = AssetDatabase.FindAssets($"{type.Name} t:MonoScript");
            if (guids == null || guids.Length == 0)
            {
                return null;
            }

            for (int i = 0; i < guids.Length; i++)
            {
                string guid = guids[i];
                if (string.IsNullOrEmpty(guid))
                {
                    continue;
                }

                string assetPath = AssetDatabase.GUIDToAssetPath(guid);
                if (string.IsNullOrEmpty(assetPath))
                {
                    continue;
                }

                MonoScript script = AssetDatabase.LoadAssetAtPath<MonoScript>(assetPath);
                if (script == null)
                {
                    continue;
                }

                Type scriptClass = script.GetClass();
                if (scriptClass == type)
                {
                    return script;
                }
            }

            return null;
        }

        private static string GetFeatureKeyByNamespaceFallback(Type type)
        {
            if (type == null)
            {
                NetLogger.LogError("LiteProtocolScanner", "GetFeatureKeyByNamespaceFallback failed");
                return "Unknown";
            }

            string ns = type.Namespace ?? string.Empty;
            if (string.IsNullOrEmpty(ns))
            {
                return SanitizeIdentifier(type.Name);
            }

            string[] segments = ns.Split('.');
            if (segments == null || segments.Length == 0)
            {
                return SanitizeIdentifier(type.Name);
            }

            for (int i = 0; i < segments.Length - 1; i++)
            {
                string current = segments[i];
                string next = segments[i + 1];

                bool isSharedPair = current == "Shared" && next == "Protocol";
                bool isServerPair = current == "Server" && (next == "Components" || next == "Modules");
                bool isClientPair = current == "Client" && (next == "Components" || next == "Modules");

                if (isSharedPair || isServerPair || isClientPair)
                {
                    if (i - 1 >= 0)
                    {
                        return SanitizeIdentifier(segments[i - 1]);
                    }

                    return SanitizeIdentifier(type.Name);
                }
            }

            if (segments.Length >= 2)
            {
                return SanitizeIdentifier(segments[segments.Length - 1]);
            }

            return SanitizeIdentifier(type.Name);
        }

        private static string AssetPathToFullPath(string assetPath)
        {
            if (string.IsNullOrEmpty(assetPath))
            {
                NetLogger.LogError("LiteProtocolScanner", "AssetPathToFullPath failed");
                return string.Empty;
            }

            string normalizedAssetPath = NormalizePath(assetPath);
            if (!normalizedAssetPath.StartsWith("Assets", StringComparison.Ordinal))
            {
                return string.Empty;
            }

            DirectoryInfo projectRoot = Directory.GetParent(Application.dataPath);
            if (projectRoot == null)
            {
                NetLogger.LogError("LiteProtocolScanner", "AssetPathToFullPath failed");
                return string.Empty;
            }

            return NormalizePath($"{projectRoot.FullName.Replace("\\", "/")}/{normalizedAssetPath}");
        }

