        private static bool WriteGeneratedShardSet(string folderPath, List<GeneratedFileRecord> records, bool forceWrite)
        {
            if (!Directory.Exists(folderPath)) Directory.CreateDirectory(folderPath);

            bool changed = false;
            var expectedPaths = new HashSet<string>(records.Select(r => NormalizePath(r.Path)));

            foreach (var oldFile in Directory.GetFiles(folderPath, "*.cs", SearchOption.TopDirectoryOnly))
            {
                string normalized = NormalizePath(oldFile);
                if (!expectedPaths.Contains(normalized))
                {
                    File.Delete(normalized);
                    if (File.Exists(normalized + ".meta")) File.Delete(normalized + ".meta");
                    changed = true;
                }
            }

            foreach (var record in records)
            {
                if (WriteToFile(record.Path, record.Content, forceWrite)) changed = true;
            }

            return changed;
        }

        private static string NormalizePath(string path) => string.IsNullOrEmpty(path) ? string.Empty : path.Replace("\\", "/");

        private static bool WriteToFile(string path, string newContent, bool forceWrite)
        {
            string normalizedPath = NormalizePath(path);
            string directory = Path.GetDirectoryName(normalizedPath);
            if (!Directory.Exists(directory)) Directory.CreateDirectory(directory);

            if (!forceWrite && File.Exists(normalizedPath) && File.ReadAllText(normalizedPath) == newContent) return false;

            File.WriteAllText(normalizedPath, newContent, Encoding.UTF8);
            return true;
        }

