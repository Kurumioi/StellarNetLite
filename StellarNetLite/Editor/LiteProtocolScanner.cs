        private static ScanResultState ScanAndGenerateProtocols(bool forceWrite)
        {
            var types = TypeCache.GetTypesWithAttribute<NetMsgAttribute>();
            var protocolList = new List<ProtocolMeta>();
            var idMap = new Dictionary<int, string>();
            var safeNameMap = new Dictionary<string, string>();
            bool hasConflict = false;

            foreach (Type type in types)
            {
                NetMsgAttribute attr = type.GetCustomAttribute<NetMsgAttribute>();
                if (attr == null) continue;

                string fullTypeName = GetCSharpTypeName(type);
                string safeName = SanitizeIdentifier(type.Name);

                if (idMap.ContainsKey(attr.Id) || safeNameMap.ContainsKey(safeName))
                {
                    NetLogger.LogError("LiteProtocolScanner", $"MsgId 或 Const 命名冲突: {attr.Id} / {safeName}");
                    hasConflict = true;
                    continue;
                }

                idMap.Add(attr.Id, fullTypeName);
                safeNameMap.Add(safeName, fullTypeName);

                protocolList.Add(new ProtocolMeta
                {
                    Id = attr.Id, SafeName = safeName, FullTypeName = fullTypeName, Scope = attr.Scope, Dir = attr.Dir,
                    FeatureKey = GetFeatureKeyByType(type)
                });
            }

            if (hasConflict) return ScanResultState.Failed;

            var protocolFiles = BuildProtocolShardFiles(protocolList);
            bool shardChanged = WriteGeneratedShardSet(ProtocolIdsFolderPath,
                protocolFiles.Where(f => f.Path.StartsWith(ProtocolIdsFolderPath)).ToList(), forceWrite);
            bool metaShardChanged = WriteGeneratedShardSet(ProtocolMetaFolderPath,
                protocolFiles.Where(f => f.Path.StartsWith(ProtocolMetaFolderPath)).ToList(), forceWrite);
            bool aggregateMsgChanged = GenerateAggregateMsgIdConstFile(protocolList, forceWrite);
            bool aggregateMetaChanged = GenerateAggregateMessageMetaRegistryFile(protocolList, forceWrite);

            return (shardChanged || metaShardChanged || aggregateMsgChanged || aggregateMetaChanged)
                ? ScanResultState.Changed
                : ScanResultState.NoChange;
        }

        private static List<GeneratedFileRecord> BuildProtocolShardFiles(List<ProtocolMeta> protocolList)
        {
            var result = new List<GeneratedFileRecord>();
            var grouped = protocolList.OrderBy(p => p.Id).GroupBy(p => string.IsNullOrEmpty(p.FeatureKey) ? "Unknown" : p.FeatureKey);

            foreach (var group in grouped)
            {
                string featureKey = SanitizeIdentifier(group.Key);
                var groupList = group.OrderBy(p => p.Id).ToList();

                var idSb = new StringBuilder();
                var metaSb1 = new StringBuilder();
                var metaSb2 = new StringBuilder();

                foreach (var proto in groupList)
                {
                    idSb.AppendLine($"        public const int {proto.SafeName} = {proto.Id};");
                    metaSb1.AppendLine(
                        $"            target[typeof({proto.FullTypeName})] = new NetMessageMeta({proto.Id}, NetScope.{proto.Scope}, NetDir.{proto.Dir});");
                    metaSb2.AppendLine($"            target[{proto.Id}] = typeof({proto.FullTypeName});");
                }

                result.Add(new GeneratedFileRecord
                {
                    Path = $"{ProtocolIdsFolderPath}/Generated_{featureKey}_MsgIds.cs",
                    Content = $@"namespace StellarNet.Lite.Shared.Generated.Protocol.MsgIds
{{
    public static class Generated_{featureKey}_MsgIds
    {{
{idSb}    }}
}}"
                });

                result.Add(new GeneratedFileRecord
                {
                    Path = $"{ProtocolMetaFolderPath}/Generated_{featureKey}_MessageMeta.cs",
                    Content = $@"using System;
using System.Collections.Generic;
using StellarNet.Lite.Shared.Core;

namespace StellarNet.Lite.Shared.Generated.Protocol.Meta
{{
    public static class Generated_{featureKey}_MessageMeta
    {{
        public static void AppendTypeToMeta(Dictionary<Type, NetMessageMeta> target)
        {{
            if (target == null) return;
{metaSb1}        }}

        public static void AppendMsgIdToType(Dictionary<int, Type> target)
        {{
            if (target == null) return;
{metaSb2}        }}
    }}
}}"
                });
            }

            return result;
        }

